# Comparison Operators

num_a = 10
num_b = 13

# equal to
print(10=='10') # FALSE


# greater than
print(num_a>num_b)

# smaller than
print(num_a<num_b)

# greater or equal to
print(num_a>=num_b)

# smaller or equal to
print(num_a<=num_b)

# not equal to
print(num_a!=num_b)

